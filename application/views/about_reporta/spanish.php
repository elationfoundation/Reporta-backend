                                <article class="article article--default">
                                    <p>
                                        La siguiente información resume cómo usar Reporta como parte de sus 
                                        protocolos de seguridad personal. Un protocolo de seguridad es un plan 
                                        para mantenerse seguro. 
                                    </p>

                                    <p>
                                        Reporta no reemplaza la capacitación en seguridad ni los servicios de 
                                        atención de emergencias. No obstante, al facilitar el envío inmediato de
                                        mensajes de Registro, Alerta y SOS a sus Círculos de contactos 
                                        personalizados, la aplicación proporciona información crítica a quienes 
                                        podrían actuar en su nombre si fuera necesario.
                                    </p>

                                    <p>                                                                       
                                        CÍRCULOS<br>
                                        La característica central de Reporta es la posibilidad de contactar a 
                                        personas que se preocupan por su seguridad. Usted puede categorizar a 
                                        sus contactos en tres Círculos distintos.  
                                    </p>

                                    <p>
                                        CÍRCULO PRIVADO<br>
                                        Su Círculo Privado es el grupo de contactos que son más cercanos a usted
                                        y que tomarán medidas en su nombre si resulta necesario. No hay ningún 
                                        límite con respecto a la cantidad de contactos que puede designar en 
                                        este grupo, pero estos contactos son en general los colaboradores más 
                                        cercanos, familiares o colegas, como su editor, su socio o un hermano.
                                    </p>

                                    <p>
                                        Si agrega una persona a este Círculo, debe darle instrucciones, 
                                        preferentemente por escrito, sobre lo que debe hacer si recibe un 
                                        mensaje de alerta de Reporta. Seguramente querrá que estos contactos 
                                        sepan lo que deben hacer si usted no responde a un Registro, envía una 
                                        Alerta, o un SOS. 
                                    </p>

                                    <p>
                                        Para usar Reporta, debe designar por lo menos a un contacto en su 
                                        Círculo Privado para que actúe como contacto de Desbloqueo de la 
                                        Aplicación. Los contactos de Desbloqueo de la Aplicación recibirán un 
                                        enlace a un código de verificación para desbloquear la aplicación si 
                                        usted no responde un mensaje de Registro o envía un SOS. Puede agregar 
                                        todos los contactos de Desbloqueo de la Aplicación que desee, pero es 
                                        necesario que haya un contacto de Desbloqueo de la Aplicación designado 
                                        en su Círculo Privado.
                                    </p>

                                    <p>
                                        Una vez que se bloquee la aplicación, no podrá acceder a ella en su 
                                        teléfono hasta que tenga el código de Desbloqueo de la Aplicación. No 
                                        hay ninguna otra forma de desbloquear la aplicación. 
                                    </p>

                                    <p>
                                        Puede crear más de un Círculo Privado, pero puede tener solo un Círculo 
                                        Privado activo por vez. Puede optar por crear diferentes Círculos 
                                        Privados según la historia que esté cubriendo o el lugar donde esté 
                                        trabajando.  
                                    </p>

                                    <p>
                                        Para su conveniencia, Reporta le preguntará si desea actualizar sus 
                                        Círculos cuando viaje 160 km (100 millas) o más desde el último lugar en
                                        el que usó la aplicación.
                                    </p>

                                    <p>
                                        CÍRCULO PÚBLICO<br>
                                        Su Círculo Público es un grupo de personas u organizaciones que pueden 
                                        estar interesadas en su seguridad. Puede incluir asociaciones 
                                        periodísticas, sindicatos u organizaciones de medios. Puede crear más de
                                        un Círculo Público, pero puede tener solo un Círculo Público activo por
                                        vez. No es obligatorio crear un Círculo Público para usar Reporta.  
                                    </p>

                                    <p>
                                        CÍRCULO SOCIAL<br>
                                        Puede conectar Reporta a sus cuentas de Facebook y Twitter. Tenga en 
                                        cuenta que cualquier publicación que haga en las redes sociales a través
                                        de Reporta podrá ser vista por terceros.
                                    </p>

                                    <p>
                                        CONCOZCA LAS TRES FUNCIONES PRINCIPALES 
                                    </p>

                                    <p>
                                        REGISTRO<br>
                                        A través de la función de Registro puede proporcionar información 
                                        personalizada sobre su ubicación actual o futura, seleccionar con qué 
                                        frecuencia desea recibir Registros, y designar a qué Círculos de 
                                        contactos se notificará. 
                                    </p>

                                    <p>
                                        Puede configurar un Registro para que comience “ahora mismo” o en el futuro.  
                                    </p>

                                    <p>
                                        También debe seleccionar una ubicación; puede hacerlo usando la función 
                                        de geolocalización o puede ingresar su ubicación manualmente.
                                    </p>

                                    <p>
                                        También puede elegir agregar una fotografía, un video o archivos de 
                                        audio para compartirlos como parte del mensaje de Registro. Estos 
                                        archivos multimedia son eliminados de la aplicación después de la 
                                        transmisión. No se puede acceder a los archivos desde la biblioteca de 
                                        medios del dispositivo.
                                    </p>

                                    <p>
                                        Usted puede seleccionar la frecuencia del Registro de una lista de 
                                        intervalos predeterminados o configurar intervalos personalizados. 
                                        También puede elegir cómo desea recibir las notificaciones (por SMS, 
                                        correo electrónico o a su teléfono inteligente).
                                    </p>

                                    <p>
                                        Para cerrar la función de Registro, debe ingresar su contraseña de Reporta.
                                    </p>

                                    <p>
                                        Si no responde a un Registro, se les enviará una alerta a todos los 
                                        Círculos seleccionados, y la aplicación se bloqueará. Reporta notificará
                                        a sus contactos de Desbloqueo de la Aplicación con un mensaje que 
                                        incluirá un enlace a un código de verificación requerido para 
                                        desbloquear la aplicación.  
                                    </p>

                                    <p>
                                        ALERTA<br>
                                        La función de Alerta le permite enviar información sobre una situación 
                                        que está enfrentando usted o un colega a los Círculos de contactos 
                                        designados. Puede usar un menú desplegable que describe posibles 
                                        incidentes; también puede ingresar información adicional. Debe 
                                        seleccionar una ubicación utilizando la función de geolocalización o 
                                        ingresar su ubicación manualmente.
                                    </p>

                                    <p>
                                        Las alertas son enviadas en forma inmediata y con la opción de agregar archivos de medios.
                                    </p>

                                    <p>
                                        Las alertas se pueden personalizar para compartirlas con diferentes Círculos.
                                    </p>

                                    <p>
                                        SOS<br>
                                        La función de enviar un SOS es visible en todas las páginas de Reporta. 
                                        Si mantiene presionado el botón de SOS durante tres segundos, se enviará
                                        un SOS solamente a su Círculo Privado.  
                                    </p>

                                    <p>
                                        Al activar la función SOS, Reporta se bloquea. Una vez que la aplicación
                                        se bloquea, solo puede ser desbloqueada mediante un proceso de 
                                        verificación implementado entre usted y sus contactos de Desbloqueo de 
                                        la Aplicación designados. Es fundamental que usted y sus contactos de 
                                        Desbloqueo de la Aplicación decidan qué harán en caso de recibir un 
                                        mensaje de SOS.
                                    </p>
