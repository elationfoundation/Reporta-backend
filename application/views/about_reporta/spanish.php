                            <article class="article article--default">
                                <p>
                                    Reporta es una aplicación de seguridad 
                                    creada por la Asociación Internacional de Mujeres en los Medios (IWMF) 
                                    para periodistas que trabajan en entornos peligrosos. Diseñada en 
                                    colaboración con periodistas destacados y expertos en seguridad global, 
                                    Reporta es la única aplicación de seguridad global integral creada 
                                    específicamente para la comunidad periodística. 
                                </p>

                                <p>
                                    Reporta permite que los periodistas 
                                    tomen el control de su seguridad personal. Al usar Reporta, pueden 
                                    enviar rápidamente notificaciones de seguridad con archivos multimedia a
                                    contactos designados a través de las tres funciones principales de la 
                                    aplicación:
                                </p>

                                <ol>
                                    <li>Activar un sistema de Registro 
                                        (Check-in) que realiza un rastreo cuando usted está trabajando en 
                                        entornos potencialmente peligrosos.</li>
                                        <li>Crear mensajes personalizados de Alerta (Alert) cuando usted o un colega estén en  riesgo potencial.</li>
                                        <li>Enviar un mensaje de SOS (SOS) simplemente tocando la pantalla del teléfono.</li>
                                    </ol>

                                    <p>
                                        Para una protección óptima, la 
                                        aplicación se bloqueará si usted envía un SOS o no responde a un 
                                        Registro con el fin de evitar accesos no deseados. Además, los archivos 
                                        transaccionales y multimedia no se almacenan en la aplicación después 
                                        del envío.  
                                    </p>

                                    <p>
                                        La aplicación incluye otras funciones para proteger los datos y la privacidad de los usuarios. Para más información, revise los <a href="<?php echo base_url().'termsandconditions'; ?>">Términos de Servicio</a> y la <a href="<?php echo base_url().'termsandconditions'; ?>">Política de Privacidad</a> de Reporta.
                                    </p>

                                    <p>
                                        Reporta no es un servicio de rescate de 
                                        emergencia y fue diseñada para ser parte, y no un reemplazo, de la 
                                        capacitación en seguridad.
                                    </p>

                                    <p>
                                        La aplicación gratuita está disponible 
                                        para dispositivos iPhone y Android en seis idiomas: árabe, inglés, 
                                        francés, hebreo, español y turco.
                                    </p>

