                            <article class="article article--default">
                                <p>
                                    Les informations suivantes décrivent comment utiliser Reporta dans le 
                                    cadre de vos protocoles de sécurité personnels. Par protocole de 
                                    sécurité, nous entendons votre stratégie de protection. 
                                </p>

                                <p>
                                    Reporta ne saurait se substituer à une formation en matière de sécurité,
                                    ni à un service de secours d’urgence. Toutefois, en vous donnant la 
                                    possibilité d’effectuer des check-in réguliers et d’envoyer des alertes 
                                    et SOS à vos cercles de contacts personnalisés, l’application fournit 
                                    des informations cruciales aux personnes qui pourraient agir en votre 
                                    nom, si besoin.
                                </p>

                                <p>                                                                       
                                    CERCLES<br>
                                    Reporta repose avant tout sur la possibilité de contacter les personnes 
                                    qui se soucient de votre sécurité. Vous pouvez classer par catégories 
                                    vos contacts dans trois cercles distincts.  
                                </p>

                                <p>
                                    CERCLE PRIVÉ<br>
                                    Votre cercle privé regroupe les contacts qui vous sont proches et 
                                    qui prendront les mesures nécessaires en votre nom, si besoin. Le nombre
                                    de contacts que vous pouvez désigner dans ce groupe est illimité ; il 
                                    s’agit généralement de vos plus proches collaborateurs, des membres de 
                                    votre famille, ou de collègues (par ex. votre rédacteur, votre 
                                    partenaire, un frère ou une sœur).
                                </p>

                                <p>
                                    Quand vous ajoutez des personnes à ce cercle, vous devez leur fournir 
                                    des instructions, de préférence par écrit, indiquant ce qu’ils doivent 
                                    faire en cas d’alerte Reporta. Ces contacts doivent savoir quoi faire si
                                    vous manquez un check-in, envoyez une alerte ou activez un SOS. 
                                </p>

                                <p>
                                    Pour utiliser Reporta, vous devez désigner au moins une personne dans 
                                    votre cercle privé qui servira de contact de déverrouillage de 
                                    l’application. Les contacts de déverrouillage de l’application recevront
                                    un lien vers un code de vérification obligatoire pour déverrouiller 
                                    l’application si vous manquez un check-in ou si vous envoyez un SOS. 
                                    Vous pouvez ajouter autant de contacts de déverrouillage que vous 
                                    voulez, sachant que l’un d’eux doit nécessairement figurer dans votre 
                                    cercle privé.
                                </p>

                                <p>
                                    Une fois l’application verrouillée, vous ne pourrez plus y accéder sur 
                                    votre téléphone sans le code de déverrouillage de l’application. Il n’y a
                                    pas d’autre moyen de déverrouiller l’application. 
                                </p>

                                <p>
                                    Vous pouvez créer plusieurs cercles privés, mais vous ne pouvez en 
                                    activer qu’un à la fois. Vous pouvez choisir de créer différents cercles
                                    privés en fonction du sujet que vous couvrez ou du lieu où vous 
                                    travaillez.  
                                </p>

                                <p>
                                    Pour mieux vous servir, Reporta vous demandera si vous voulez actualiser
                                    vos cercles quand vous vous trouvez à 150 kilomètres ou plus du dernier
                                    endroit où vous avez utilisé l’application.
                                </p>

                                <p>
                                    CERCLE PUBLIC<br>
                                    Votre cercle public est un groupe de personnes ou d’organisations qui 
                                    s’intéressent à votre sécurité (par ex. associations de journalisme, 
                                    syndicats ou organes de presse). Vous pouvez créer plusieurs cercles 
                                    publics, mais vous ne pouvez en activer qu’un à la fois. La création 
                                    d’un cercle public n’est pas indispensable pour utiliser Reporta.  
                                </p>

                                <p>
                                    CERCLE SOCIAL<br>
                                    Vous pouvez connecter Reporta à vos comptes Facebook et Twitter. Notez 
                                    que tout ce que vous publierez sur les réseaux sociaux via Reporta sera 
                                    visible par les autres.
                                </p>

                                <p>
                                    FAMILIARISEZ-VOUS AVEC LES TROIS FONCTIONS CLÉS
                                </p>

                                <p>
                                    CHECK-IN<br>
                                    Avec la fonction Check-in, vous pouvez fournir des informations 
                                    personnalisées au sujet de votre position actuelle ou future, 
                                    sélectionner la fréquence à laquelle vous souhaitez donner signe de vie 
                                    et désigner les cercles de contacts à avertir. 
                                </p>

                                <p>
                                    Vous pouvez programmer un check-in pour qu’il démarre « maintenant » ou plus tard.  
                                </p>

                                <p>
                                    Vous devez aussi sélectionner une position ; pour cela, vous pouvez 
                                    utiliser la géolocalisation ou bien saisir votre position manuellement.
                                </p>

                                <p>
                                    Vous pouvez également choisir d’ajouter des photos, des vidéos ou du son
                                    à partager dans votre message de check-in. Ces fichiers multimédias 
                                    sont supprimés de l’application une fois qu’ils ont été envoyés. Les 
                                    fichiers ne sont pas accessibles depuis la bibliothèque multimédia de 
                                    l’appareil.
                                </p>

                                <p>
                                    Vous pouvez sélectionner la fréquence des notifications de check-in à 
                                    partir d’une liste ou définir vous-même ces intervalles. Vous pouvez 
                                    aussi choisir sous quelle forme vous souhaitez recevoir ces 
                                    notifications (SMS, email ou notification sur Smartphone).
                                </p>

                                <p>
                                    Pour fermer un check-in, vous devez saisir votre mot de passe Reporta.
                                </p>

                                <p>
                                    Si vous manquez un check-in, une alerte sera envoyée à tous les cercles 
                                    sélectionnés et l’application se verrouillera. Reporta préviendra vos 
                                    contacts de déverrouillage de l’application à l’aide d’un message 
                                    contenant un lien vers un code de vérification nécessaire pour le 
                                    déverrouillage de l’application.  
                                </p>

                                <p>
                                    ALERTES<br>
                                    La fonction Alerte permet d’envoyer aux cercles de contacts désignés des
                                    informations à propos d’une situation dans laquelle vous ou un confrère
                                    journaliste vous trouvez. Vous pouvez utiliser un menu déroulant pour 
                                    décrire des incidents potentiels ; vous pouvez aussi choisir de saisir 
                                    un complément d’information. Vous devez sélectionner une position, à la 
                                    main ou à l'aide de la fonction géolocalisation.
                                </p>

                                <p>
                                    Les alertes sont envoyées instantanément, avec la possibilité d’ajouter des fichiers multimédias.
                                </p>

                                <p>
                                    Vous pouvez personnaliser les alertes pour les partager avec différents cercles.
                                </p>

                                <p>
                                    SOS<br>
                                    La fonction SOS apparaît sur chaque page de Reporta. Si vous appuyez sur
                                    le bouton SOS pendant trois secondes, un SOS sera envoyé uniquement à 
                                    votre cercle privé.  
                                </p>

                                <p>
                                    L’activation de la fonction SOS verrouille Reporta. Une fois 
                                    l’application verrouillée, elle ne peut être déverrouillée qu'en suivant
                                    la procédure établie entre vous et vos contacts de déverrouillage. Il 
                                    est important que vous et vos contacts de déverrouillage de 
                                    l’application discutiez des mesures à prendre en cas d’envoi d’un SOS.
                                </p>
