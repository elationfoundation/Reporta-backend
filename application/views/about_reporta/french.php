                            <article class="article article--default">
                                <p>
                                    Créée par l’International Women’s Media 
                                    Foundation (IWMF), l’application de sécurité Reporta s’adresse aux 
                                    journalistes qui travaillent dans des milieux dangereux. Conçue en 
                                    collaboration avec de grands journalistes et experts en sécurité 
                                    internationale, Reporta est la seule application de sécurité complète 
                                    s’adressant expressément à la communauté journalistique à l’échelle 
                                    mondiale. 
                                </p>

                                <p>
                                    Reporta permet aux journalistes de 
                                    prendre leur sécurité personnelle en main. Avec Reporta, vous pouvez 
                                    envoyer rapidement des notifications de sécurité contenant des fichiers 
                                    multimédias à des contacts désignés grâce aux trois fonctions clés de 
                                    l’application :
                                </p>

                                <ol>
                                    <li>Activez un système de check-in qui crée une piste claire quand vous travaillez dans des milieux potentiellement dangereux.</li>
                                    <li>Créez des messages d’alerte personnalisés qui seront envoyés dans le cas où vous ou un collègue vous retrouveriez en danger.</li>
                                    <li>Envoyez un message de détresse SOS en un geste depuis votre téléphone.</li>
                                </ol>

                                <p>
                                    Pour une sécurité optimale, 
                                    l’application se verrouille en cas d’envoi d’un SOS ou si vous manquez 
                                    un check-in, pour empêcher tout accès indésirable. Par ailleurs, les 
                                    fichiers de transactions et multimédias ne sont pas stockés dans 
                                    l’application une fois qu’ils ont été envoyés.  
                                </p>

                                <p>
                                    L’application comprend d’autres fonctionnalités permettant de protéger les données et la vie privée des utilisateurs. Pour plus d’informations, consultez les <a href="<?php echo base_url().'termsandconditions'; ?>">conditions de service</a> et la <a href="<?php echo base_url().'termsandconditions'; ?>">politique de confidentialité</a> de Reporta.
                                </p>

                                <p>
                                    Reporta n’est pas un service de secours 
                                    d’urgence ; l’application a été conçue non pas pour remplacer, mais pour
                                    faire partie intégrante d’une formation en matière de sécurité.
                                </p>

                                <p>
                                    Conçue pour iPhone et Android, 
                                    l’application gratuite existe en six langues : anglais, arabe, espagnol,
                                    français, hébreu et turc.
                                </p>

