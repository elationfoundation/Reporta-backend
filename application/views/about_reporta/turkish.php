                            <article class="article article--default">

                                <p>
                                    Kişisel güvenlik protokollerinizin bir parçası olarak Reporta’nın nasıl 
                                    kullanılacağı aşağıda açıklanmıştır. Güvenlik protokolü, güvende kalma 
                                    planınızdır. 
                                </p>

                                <p>
                                    Reporta güvenlik eğitiminin veya bir acil yardım servisinin alternatifi 
                                    değildir. Ancak özel kişi halkalarınıza anında Check in, Uyarı ve İmdat 
                                    mesajlarının gönderilmesini sağlayan uygulama, gerektiğinde sizin 
                                    adınıza hareket edebilecek kişilere kritik bilgiler sağlar.
                                </p>

                                <p>                                                                       
                                    HALKALAR<br>
                                    Reporta’nın en önemli özelliği, güvenliğinize önem veren kişilerle 
                                    iletişim kurma imkanıdır. Kişilerinizi üç ayrı Halka olarak 
                                    sınıflandırabilirsiniz.  
                                </p>

                                <p>
                                    ÖZEL HALKA<br>
                                    Özel Halkanız, size en yakın olan ve gerektiğinde sizin adınıza harekete
                                    geçebilecek kişiler grubudur. Bu grupta tanımlayabileceğiniz kişi 
                                    sayısıyla ilgili bir sınır bulunmamaktadır ancak bu kişiler genellikle 
                                    editörünüz, eşiniz veya kardeşiniz gibi en yakın çalışanlar, aile 
                                    üyeleri veya çalışma arkadaşlarınızdır.
                                </p>

                                <p>
                                    Bu halkaya kişiler ekliyorsanız, Reporta’dan uyarı aldıklarında ne 
                                    yapmaları gerektiğine ilişkin, tercihen yazılı bir bilgilendirme 
                                    sağlamalısınız. Bir Check in’i atlamanız, bir Uyarı veya İmdat mesajı 
                                    göndermeniz durumunda bu kişilerin ne yapmaları gerektiğini bilmelerini 
                                    istersiniz. 
                                </p>

                                <p>
                                    Reporta’yı kullanmak için Özel Halkanızda Uygulama Kilidi Açma rolünü 
                                    görecek en az bir kişi seçmelisiniz. Uygulama Kilidini Açma rolündeki 
                                    kişiler bir Check in kaçırdığınızda veya bir İmdat mesajı 
                                    gönderdiğinizde uygulama kilidini açmak için gereken doğrulama kodunun 
                                    bağlantısını alacaktır. Uygulama Kilidi Açma rolünü istediğiniz kadar 
                                    fazla kişiye verebilirsiniz ancak Uygulama Kilidi Açma rolü verilen kişi
                                    Özel Halkanızda yer almalıdır.
                                </p>

                                <p>
                                    Kilitlendikten sonra, Uygulama Kilidini Açma kodunu alana kadar 
                                    telefonunuzda uygulamaya erişim sağlayamazsınız. Uygulama kilidini 
                                    açmanın başka bir yolu bulunmamaktadır. 
                                </p>

                                <p>
                                    Birden fazla Özel Halka oluşturabilirsiniz ancak bir seferde sadece bir 
                                    Özel Halkayı etkinleştirebilirsiniz. Yaptığınız habere veya çalıştığınız
                                    yere bağlı olarak farklı Özel Halkalar oluşturmayı tercih 
                                    edebilirsiniz.  
                                </p>

                                <p>
                                    Size kolaylık sağlamak amacıyla Reporta, uygulamayı en son kullandığınız
                                    konumdan 100 mil veya daha uzağa gittiğinizde Halkalarınızı güncellemek
                                    isteyip istemediğinizi soracaktır.
                                </p>

                                <p>
                                    GENEL HALKA<br>
                                    Genel Halkanız, güvenliğinizle ilgilenebilecek kişi veya kuruluşları 
                                    kapsar. Bunlar arasında gazeteciler vakıfları, birlikleri veya medya 
                                    organizasyonları bulunabilir. Birden fazla Genel Halka 
                                    oluşturabilirsiniz ancak bir seferde sadece bir Genel Halkayı 
                                    etkinleştirebilirsiniz. Reporta’yı kullanmak için Genel Halka oluşturmak
                                    zorunlu değildir.  
                                </p>

                                <p>
                                    SOSYAL HALKA<br>
                                    Reporta’yı Facebook ve Twitter hesaplarınızla bağlantılandırabilirsiniz.
                                    Reporta vasıtasıyla sosyal medyada yayınladığınız mesajların başkaları 
                                    tarafından görülebileceğini unutmayın.
                                </p>

                                <p>
                                    ÜÇ ÖNEMLİ FONKSİYON HAKKINDA BİLGİ EDİNİN
                                </p>

                                <p>
                                    CHECK IN<br>
                                    Check in fonksiyonunu kullanarak mevcut veya gelecekteki konumunuz 
                                    hakkında özel bilgiler sağlayabilir, ne sıklıkla Check in yapmak 
                                    istediğinizi seçebilir ve hangi Halkalara bildirim yapılacağını 
                                    belirleyebilirsiniz. 
                                </p>

                                <p>
                                    “Hemen” veya gelecekte başlayacak bir Check in belirleyebilirsiniz.  
                                </p>

                                <p>
                                    Ayrıca bir konum seçmelisiniz ya da bir coğrafi konum kullanarak veya konumunuzu elle yazarak konum seçebilirsiniz.
                                </p>

                                <p>
                                    Check in mesajının bir parçası olarak paylaşılacak fotoğraf, video veya 
                                    ses dosyaları eklemeyi tercih edebilirsiniz. Bu multimedya dosyaları 
                                    iletildikten sonra uygulamadan silinir. Dosyalara cihazın temel ortam 
                                    kitaplığından erişilemez.
                                </p>

                                <p>
                                    Listeden veya belirlenen özel aralıklardan Check in istemi sıklığını 
                                    seçebilirsiniz. İstemleri (SMS, e-posta veya akıllı telefon 
                                    bildirimleri) nasıl almak istediğinizi de seçebilirsiniz.
                                </p>

                                <p>
                                    Check in’i kapatmak için Reporta parolanızı girmelisiniz.
                                </p>

                                <p>
                                    Bir Check in’i atlarsanız, bütün seçilen Halkalara bir uyarı gönderilir 
                                    ve uygulama kilitlenir. Reporta, Uygulama Kilidi Açma kişilerinizi 
                                    uygulama kilidini açmak için gereken doğrulama kodu bağlantısını içeren 
                                    bir mesajla bilgilendirecektir.  
                                </p>

                                <p>
                                    UYARILAR<br>
                                    Uyarı fonksiyonu, sizin veya bir gazeteci meslektaşınızın karşılaştığı 
                                    durum hakkında kişi Halkalarına bilgi göndermenizi sağlar. Olası 
                                    olayları açıklayan bir açılır menüyü kullanabilirsiniz; ayrıca ek 
                                    bilgiler girmeyi de tercih edebilirsiniz. Coğrafi konumu kullanarak veya
                                    manuel olarak girerek bir konum seçilmelidir.
                                </p>

                                <p>
                                    Uyarılar gecikme olmaksızın ve medya dosyaları ekleme seçeneğiyle gönderilir.
                                </p>

                                <p>
                                    Farklı Halkalarla paylaşmak üzere uyarılar gönderilebilir.
                                </p>

                                <p>
                                    İMDAT<br>
                                    İmdat özelliği Reporta’nın her bir sayfasında görülür. İmdat düğmesine 
                                    üç saniye basar ve basılı tutarsanız, İmdat mesajı sadece Özel Halkanıza
                                    gönderilir.  
                                </p>

                                <p>
                                    İmdat özelliği etkinleştirildiğinde Reporta kilitlenir. Kilitlendikten 
                                    sonra, sadece sizinle belirlenen Uygulama Kilidini Açma kişileri 
                                    arasında uygulanan bir doğrulama işlemi ile kilit açılabilir. Sizin ve 
                                    Uygulama Kilidi Açma kişilerinizin bir İmdat mesajı almaları durumunda 
                                    ne yapacaklarını görüşmesi son derece önemlidir.
                                </p>
