                            <article class="article article--default">
                                
                                <p>
                                    The following information outlines how to 
                                    use Reporta as part of your personal security protocols. A security 
                                    protocol is your plan to stay safe. 
                                </p>
                                <p>
                                </p><p>                        
                                Reporta is not a replacement for security training or an emergency 
                                relief service. However, by facilitating immediate Check-in, Alert and 
                                SOS messages to your customized contact Circles, the app provides 
                                critical information to those who could act on your behalf if needed.
                            </p>

                            <p>                                                                      
                                CIRCLES<br>
                                The heart of Reporta is the ability to contact the people who care about
                                your safety. You can categorize your contacts into three distinct 
                                Circles.  
                            </p>

                            <p>
                                PRIVATE CIRCLE<br>
                                Your Private Circle is the group of contacts with whom you are closest 
                                and those who will take action on your behalf if needed. There is no 
                                limit to the number of contacts you designate in this group, but these 
                                contacts are usually your closest associates, family members or 
                                colleagues, such as your editor, partner or a sibling.
                            </p>

                            <p>
                                If you are adding individuals to this Circle, you should provide them 
                                with instructions, preferably written, about what to do if alerted by 
                                Reporta. You will want these contacts to know what to do if you have 
                                missed a Check-in, sent an Alert, or activated an SOS. 
                            </p>
                            <p>
                                To use Reporta, you must designate at least one contact in your Private 
                                Circle to serve as your App-Unlock contact. App-Unlock contacts will 
                                receive a link to a verification code required to unlock the app if you 
                                miss a Check-in or issue an SOS. You may add as many App-Unlock contacts
                                as you wish, but a designated App-Unlock contact must be included in 
                                your Private Circle.
                            </p>
                            <p>
                                Once locked, you will not be able to access the app on your phone until 
                                you have the App-Unlock code. There is no other way to unlock the app. 
                            </p>
                            <p>
                                You may create more than one Private Circle, but only one Private Circle
                                can be active at any given time. You may choose to create different 
                                Private Circles based on the story you are covering or where you are 
                                working.  
                            </p>

                            <p>
                                For you convenience, Reporta will ask you if you want to update your 
                                Circles when you travel 100 miles or more from the last place you used 
                                the app.
                            </p>

                            <p>
                                PUBLIC CIRCLE<br>
                                Your Public Circle is a group of individuals or organizations that may 
                                be interested in your security. This may include journalism 
                                associations, unions or media organizations. You may create more than 
                                one Public Circle, but only one Public Circle can be active at any given
                                time. Creating a Public Circle is not required to use Reporta.  
                            </p>

                            <p>
                                SOCIAL CIRCLE<br>
                                You can connect Reporta to your Facebook and Twitter accounts. Note that
                                anything you post to social media through Reporta can be seen by 
                                others.
                            </p>

                            <p>
                                FAMILIARIZE YOURSELF WITH THE THREE KEY FUNCTIONS 
                            </p>

                            <p>
                                CHECK-IN<br>
                                Through the Check-in function you can provide customized information 
                                about your current or future location, select how often you want to 
                                Check-in, and designate which contact Circles to notify. 
                            </p>

                            <p>
                                You can set a Check-in to begin “right now” or in the future.  
                            </p>

                            <p>
                                You must also select a location and can do so either by using geolocation or by entering your location manually.
                            </p>

                            <p>
                                You may also choose to add photo, video or audio files to share as 
                                part of the Check-in message. These multimedia files are removed from 
                                the app after transmission. Files cannot be accessed from the device’s 
                                underlying media library.
                            </p>

                            <p>
                                You can select Check-in prompt frequency from a list or set custom 
                                intervals. You can also choose how you wish to receive prompts (SMS, 
                                email or smartphone notification).
                            </p>

                            <p>
                                To close a Check-in, you must enter your Reporta password.
                            </p>

                            <p>
                                If you miss a Check-in, an alert will be sent to all selected Circles, 
                                and the app will lock. Reporta will notify your App-Unlock contacts with
                                a message containing a link to a verification code required to unlock 
                                the app.  
                            </p>

                            <p>
                                ALERTS<br>
                                The Alert function allows you to send information about a situation you 
                                or a fellow journalist is facing to designated contact Circles. You may 
                                use a drop down menu describing possible incidents; you may also choose 
                                to enter additional information. A location must be selected either 
                                using geolocation or entered manually.
                            </p>

                            <p>
                                Alerts are sent without delay and with the option to add media files.
                            </p>

                            <p>
                                Alerts can be customized to share with different Circles.
                            </p>

                            <p>
                                SOS<br>
                                The SOS feature is visible on every page of Reporta. If you press and 
                                hold the SOS button for three seconds, an SOS will be sent only to your 
                                Private Circle.  
                            </p>

                            <p>
                                Activating the SOS feature locks Reporta. Once locked, it can only be 
                                unlocked with a verification process implemented between you and your 
                                designated App-Unlock contacts. It is critical that you and your 
                                App-Unlock contacts discuss what to do in the event they receive an SOS 
                                message.
                            </p>
