                                <p>
                                    Reporta is a security app created by the
                                    International Women’s Media Foundation (IWMF) for journalists working 
                                    in dangerous environments. Designed in consultation with leading 
                                    journalists and global security experts, Reporta is the only 
                                    comprehensive global security app created specifically for the media 
                                    community. 
                                </p>

                                <p>
                                    Reporta empowers journalists to take 
                                    control of their personal safety. Using Reporta, you can quickly issue 
                                    security notifications containing multimedia files to designated 
                                    contacts through the app’s three key functions:
                                </p>

                                <ol>
                                    <li>Activate a Check-in system that creates a trail when you are working in potentially dangerous environments.</li>
                                    <li>Create customized Alert messages when you or a colleague may be at risk.</li>
                                    <li>Issue an SOS distress message with one simple touch of the phone.</li>
                                </ol>

                                <p>
                                    For optimal security, the app will lock 
                                    if an SOS is issued or a Check-in is missed to prevent unwanted access. 
                                    Additionally, transactional and multimedia files are not stored in the 
                                    app after transmission.  
                                </p>

                                <p>
                                    The app includes other features to 
                                    safeguard the data and privacy of users. For more information, review 
